package cn.news.entity;
/**
 * 主题实体
 * @author lxh
 *
 */
public class Topic {
	private int tid;		//主题id
	private String tname;	//主题名称
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
}	
